    private void JB_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_pesquisarActionPerformed
        // Obt√©m o c√≥digo digitado no JTextField de pesquisa
        String codigoPesquisa = this.JTF_pesquisa.getText().trim();

        // Usa o m√©todo do controle para buscar o relat√≥rio do produto
        String relatorio = controleProduto.getRelatorioProduto(codigoPesquisa);

        if (relatorio != null) {
            // Exibe o relat√≥rio no JTextArea
            this.JTA_relatorio.setText(relatorio);
        } else {
            // Se n√£o encontrou o produto, exibe uma mensagem de erro
            JOptionPane.showMessageDialog(rootPane, "Produto n√£o encontrado com o c√≥digo: " + codigoPesquisa, "Produto N√£o Encontrado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JB_pesquisarActionPerformed

